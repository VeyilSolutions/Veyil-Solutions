/* ─────────────────────────────
   FAST LOCAL RESPONSE MATCHER
───────────────────────────── */

export function matchLocal(msg, lang) {
  const m = msg.toLowerCase();
  const r = LOCAL[lang] || LOCAL.en;

  // Greeting
  if (/\b(hi|hello|hey|hai|helo|start|வணக்கம்|ஹலோ)\b/.test(m))
    return r.hello;

  // Affirmative response
  if (/\b(yes|ok|sure|yeah|yep|okay|ஆம்|சரி)\b/.test(m))
    return r.free;

  // Business intent
  if (/website|store|shop|online shop|ecommerce|instagram shop|whatsapp shop/.test(m))
    return r.pricing;

  // Product businesses
  if (/clothing|dress|jewelry|handmade|bakery|cosmetics|beauty|food/.test(m))
    return r.free;

  // Pricing
  if (/price|cost|pricing|rate|how much|கட்டணம்|விலை|எவ்வளவு/.test(m))
    return r.pricing;

  // Packages
  if (/launch|8000|8,000/.test(m))
    return r.launch;

  if (/growth|15000|15,000/.test(m))
    return r.growth;

  // Support
  if (/support|3000|monthly|மாதாந்திர/.test(m))
    return r.support;

  // Delivery
  if (/deliver|how long|days|time|நாட்கள்|எத்தனை/.test(m))
    return r.delivery;

  // Shopify
  if (/shopify|platform|subscription|1999/.test(m))
    return r.shopify;

  // Additional costs
  if (/additional|extra cost|separate|தனி|கூடுதல்/.test(m))
    return r.additional;

  // Contact
  if (/contact|reach|call|phone|number|தொடர்பு|எண்/.test(m))
    return r.contact;

  // Free sample
  if (/free|sample|இலவச|மாதிரி|preview/.test(m))
    return r.free;

  return null;
}